"""Listas dentro de listas(Matrizes)"""

matriz = [["bulbasaur","yversaur","venusaur"],["squirtle","wartotle","blastoise"],["charmander","chameleon","charizard"]]

print(matriz[1][0]) #squirtle
print(matriz[0][1]) #yversaur
print(matriz[2][2]) #charizard

